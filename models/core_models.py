class WorldState(BaseModel):
    """
    WorldState - The Brain's understanding of the current state.
    
    This represents the "world model" maintained by the Brain.
    """
    state_id: str = Field(..., description="Unique state identifier")
    entities: Dict[str, Any] = Field(default_factory=dict)
    relationships: Dict[str, List[str]] = Field(default_factory=dict)
    current_goals: List[str] = Field(default_factory=list)
    active_constraints: List[str] = Field(default_factory=list)
    timestamp: datetime = Field(default_factory=datetime.now)


class BrainThought(BaseModel):
    """
    BrainThought - A single unit of reasoning from the Brain.
    
    For observability: logs the "Slow Thinking" process.
    """
    thought_id: str
    plan_id: Optional[str] = None
    thought_type: str  # perception, reasoning, planning, evaluation, decision
    content: str
    confidence: float = Field(ge=0, le=1)
    timestamp: datetime = Field(default_factory=datetime.now)
    metadata: Dict[str, Any] = Field(default_factory=dict)


# ============================================================
# HELPER FUNCTIONS
# ============================================================

def create_task_request(
    task_id: str,
    tool_id: str,
    parameters: Dict[str, Any],
    priority: TaskPriority = TaskPriority.NORMAL,
    **kwargs
) -> TaskRequest:
    """Factory function to create a TaskRequest with defaults"""
    return TaskRequest(
        task_id=task_id,
        tool_id=tool_id,
        parameters=parameters,
        priority=priority,
        **kwargs
    )


def create_plan(
    plan_id: str,
    objective: str,
    steps: List[ExecutionStep],
    reasoning: str,
    **kwargs
) -> Plan:
    """Factory function to create a Plan with required fields"""
    return Plan(
        plan_id=plan_id,
        objective=objective,
        steps=steps,
        reasoning=reasoning,
        **kwargs
    )


def create_tool_result(
    tool_id: str,
    task_id: str,
    status: TaskStatus,
    output: Any = None,
    **kwargs
) -> ToolResult:
    """Factory function to create a ToolResult"""
    return ToolResult(
        tool_id=tool_id,
        task_id=task_id,
        status=status,
        output=output,
        **kwargs
    )
