class StepDependency(BaseModel):
    """Defines dependencies between steps in a plan"""
    depends_on: List[str] = Field(default_factory=list, description="Step IDs this depends on")
    condition: Optional[str] = Field(None, description="Condition for execution")
    timeout_seconds: Optional[int] = None


class ExecutionStep(BaseModel):
    """
    A single step in a plan.
    
    Each step is a discrete unit of work that a Tentacle can execute.
    """
    step_id: str = Field(..., description="Unique step identifier")
    order: int = Field(..., description="Execution order (0-indexed)")
    
    # What to execute
    tool_id: str = Field(..., description="Which tool to use")
    parameters: Dict[str, Any] = Field(default_factory=dict)
    description: str = Field(..., description="What this step does")
    
    # Dependencies
    dependencies: StepDependency = Field(default_factory=StepDependency)
    
    # Expected output schema
    expected_output_type: Optional[str] = None
    output_key: Optional[str] = Field(None, description="Key to store result under")
    
    # On failure
    on_failure: str = Field(default="abort", description="abort|retry|skip")
    max_retries: int = Field(default=3)


class Plan(BaseModel):
    """
    Plan - The complete execution blueprint generated by the Brain.
    
    This is the primary output of the Decision Brain. It contains:
    1. A sequence of steps (ExecutionSteps)
    2. Overall strategy and reasoning
    3. Success criteria
    4. Fallback plans
    
    Example:
        plan = Plan(
            plan_id="plan_xyz",
            objective="Gather competitor pricing data",
            steps=[
                ExecutionStep(step_id="s1", order=0, tool_id="web_scraper", ...),
                ExecutionStep(step_id="s2", order=1, tool_id="data_processor", ...),
            ]
        )
    """
    model_config = ConfigDict(
        json_schema_extra={
            "example": {
                "plan_id": "plan_abc",
                "objective": "Analyze market trends",
                "steps": [
                    {
                        "step_id": "s1",
                        "order": 0,
                        "tool_id": "web_scraper",
                        "description": "Scrape market data"
                    }
                ]
            }
        }
    )
    
    # Plan identification
    plan_id: str = Field(..., description="Unique plan identifier")
    
    # What the plan achieves
    objective: str = Field(..., description="High-level objective")
    description: Optional[str] = Field(None, description="Detailed description")
    
    # Steps to execute
    steps: List[ExecutionStep] = Field(..., description="Ordered execution steps")
    execution_mode: ExecutionMode = Field(default=ExecutionMode.SEQUENTIAL)
    
    # Status tracking
    status: PlanStatus = Field(default=PlanStatus.DRAFT)
    
    # Brain's reasoning (for observability)
    reasoning: str = Field(..., description="Why this plan was chosen")
    alternatives_considered: List[str] = Field(default_factory=list, description="Other plans rejected")
    simulation_results: Optional[Dict[str, Any]] = Field(None, description="Simulation outputs")
    
    # Success criteria
    success_criteria: List[str] = Field(default_factory=list)
    expected_outcome: Optional[Dict[str, Any]] = None
    
    # Fallback & error handling
    fallback_plan_id: Optional[str] = Field(None, description="Alternative plan if this fails")
    max_execution_time_seconds: Optional[int] = None
    
    # Validation
    is_validated: bool = Field(default=False)
    validation_errors: List[str] = Field(default_factory=list)
    
    # Ethics & constraints
    ethics_approved: bool = Field(default=False)
    ethics_notes: Optional[str] = None
    constraints_satisfied: bool = Field(default=True)
    
    # Metadata
    created_at: datetime = Field(default_factory=datetime.now)
    created_by: str = Field(default="brain")
    updated_at: datetime = Field(default_factory=datetime.now)
    version: int = Field(default=1)
    
    # Results (filled as plan executes)
    completed_steps: List[str] = Field(default_factory=list)
    step_results: Dict[str, ToolResult] = Field(default_factory=dict)
    final_output: Optional[Any] = Field(None)


class PlanValidation(BaseModel):
    """Validation result for a plan"""
    is_valid: bool
    errors: List[str] = Field(default_factory=list)
    warnings: List[str] = Field(default_factory=list)
    suggestions: List[str] = Field(default_factory=list)


# ============================================================
# COMMUNICATION MESSAGES
# ============================================================

class ExecutionRequest(BaseModel):
    """
    ExecutionRequest - Wraps a task for network/API transport.
    
    This is the formal "envelope" for sending work to Tentacles.
    """
    request_id: str = Field(..., description="Unique request ID")
    task: TaskRequest
    auth_token: Optional[str] = Field(None, exclude=True)
    timeout_seconds: int = Field(default=300)
    created_at: datetime = Field(default_factory=datetime.now)


class ExecutionResponse(BaseModel):
    """
    ExecutionResponse - Wraps a result for returning to the Brain.
    
    This is the formal "envelope" for returning work from Tentacles.
    """
    request_id: str = Field(..., description="Correlates with ExecutionRequest")
    task_id: str = Field(..., description="Which task this responds to")
    result: ToolResult
    received_at: datetime = Field(default_factory=datetime.now)


# ============================================================
# BRAIN STATE MODELS
