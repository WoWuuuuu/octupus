"""
Octopus Core Data Models - Pydantic Definitions

This module defines the core data transfer objects (DTOs) for communication
between the Decision Brain (Head) and Execution Tentacles.

These models ensure type safety and clear contract between layers.
"""

from datetime import datetime
from enum import Enum
from typing import Any, Dict, List, Optional, Union
from pydantic import BaseModel, Field, ConfigDict


# ============================================================
# ENUMS
# ============================================================

class TaskPriority(str, Enum):
    """Task priority levels - used by the Brain to prioritize tentacle work"""
    CRITICAL = "critical"    # Must execute immediately
    HIGH = "high"           # Execute soon
    NORMAL = "normal"       # Standard priority
    LOW = "low"             # Can wait

class TaskStatus(str, Enum):
    """Task execution status"""
    PENDING = "pending"           # Queued, not started
    RUNNING = "running"          # Currently executing
    COMPLETED = "completed"       # Successfully finished
    FAILED = "failed"            # Execution failed
    CANCELLED = "cancelled"      # Cancelled by Brain
    RETRYING = "retrying"       # Attempting to recover

class ToolCategory(str, Enum):
    """Categories of tools available to tentacles"""
    WEB_SCRAPING = "web_scraping"
    DATABASE = "database"
    CODE_EXECUTION = "code_execution"
    EMAIL = "email"
    API_CALL = "api_call"
    FILE_SYSTEM = "file_system"
    MESSAGE_QUEUE = "message_queue"
    CUSTOM = "custom"

class PlanStatus(str, Enum):
    """Status of a plan generated by the Brain"""
    DRAFT = "draft"             # Being constructed
    VALIDATED = "validated"      # Passed validation
    APPROVED = "approved"        # Approved by Brain
    REJECTED = "rejected"       # Rejected (ethics/constraints)
    EXECUTING = "executing"     # Currently being executed
    COMPLETED = "completed"      # Successfully completed
    PARTIALLY_COMPLETED = "partially_completed"
    FAILED = "failed"

class ExecutionMode(str, Enum):
    """How the plan should be executed"""
    SEQUENTIAL = "sequential"   # One step at a time
    PARALLEL = "parallel"       # All steps at once
    CONDITIONAL = "conditional" # Based on results


# ============================================================
# TOOL RESULT MODELS (Tentacle → Brain)
# ============================================================

class ToolMetadata(BaseModel):
    """Metadata about the tool that was executed"""
    tool_id: str = Field(..., description="Unique identifier of the tool")
    tool_name: str = Field(..., description="Human-readable tool name")
    category: ToolCategory = Field(..., description="Tool category")
    version: str = Field(default="1.0.0", description="Tool version")
    execution_time_ms: Optional[float] = Field(None, description="Execution time in milliseconds")


class ToolParameter(BaseModel):
    """Definition of a tool parameter"""
    name: str
    type: str
    description: str
    required: bool = True
    default: Optional[Any] = None
    options: Optional[List[str]] = None


class ToolDefinition(BaseModel):
    """Complete definition of an available tool"""
    model_config = ConfigDict(extra='allow')
    
    tool_id: str
    name: str
    description: str
    category: ToolCategory
    parameters: List[ToolParameter] = Field(default_factory=list)
    return_type: str = "any"
    constraints: List[str] = Field(default_factory=list)
    timeout_seconds: int = 60
    rate_limit: Optional[int] = None
    version: str = "1.0.0"


class ToolResult(BaseModel):
    """
    ToolResult - The data package a Tentacle sends back to the Brain.
    
    This is the fundamental "data packet" that flows from Execution → Decision.
    
    Example:
        result = ToolResult(
            tool_id="web_scraper",
            status="success",
            output={"content": "Scraped HTML...", "urls_found": 5}
        )
    """
    model_config = ConfigDict(
        json_schema_extra={
            "example": {
                "tool_id": "web_scraper",
                "status": "success",
                "output": {"content": "...", "count": 42},
                "execution_time_ms": 1234.5
            }
        }
    )
    
    # Core identification
    tool_id: str = Field(..., description="Which tool generated this result")
    task_id: str = Field(..., description="Which task this result belongs to")
    
    # Execution status
    status: TaskStatus = Field(..., description="Execution status")
    error: Optional[str] = Field(None, description="Error message if failed")
    error_code: Optional[str] = Field(None, description="Machine-readable error code")
    
    # Output
    output: Optional[Any] = Field(None, description="The actual output data")
    partial_output: Optional[Any] = Field(None, description="Partial results if streaming")
    
    # Metadata
    metadata: Dict[str, Any] = Field(default_factory=dict)
    execution_time_ms: Optional[float] = Field(None, description="Execution duration")
    timestamp: datetime = Field(default_factory=datetime.now)
    
    # Retry information
    retry_count: int = Field(default=0, description="Number of retries attempted")
    max_retries: int = Field(default=3, description="Maximum retries allowed")
    
    # Context
    logs: List[str] = Field(default_factory=list, description="Execution logs")
    warnings: List[str] = Field(default_factory=list, description="Non-fatal warnings")


# ============================================================
# TASK REQUEST MODELS (Brain → Tentacle)
# ============================================================

class TaskConstraints(BaseModel):
    """Constraints and guardrails for task execution"""
    max_execution_time_seconds: Optional[int] = None
    max_cost: Optional[float] = None
    allowed_tools: Optional[List[str]] = None
    blocked_tools: Optional[List[str]] = None
    require_approval_before: Optional[List[str]] = None
    retry_policy: Optional[Dict[str, Any]] = None


class TaskContext(BaseModel):
    """Context information passed with the task"""
    user_id: Optional[str] = None
    session_id: Optional[str] = None
    conversation_history: Optional[List[Dict[str, Any]]] = None
    entity_state: Optional[Dict[str, Any]] = Field(default_factory=dict)
    custom: Optional[Dict[str, Any]] = Field(default_factory=dict)


class TaskRequest(BaseModel):
    """
    TaskRequest - The command packet the Brain sends to the Tentacles.
    
    This is how the Brain tells the Tentacles WHAT to do and HOW to do it.
    
    Key Design Decisions:
    1. Clear task_id for tracking
    2. Explicit parameters (no ambiguity)
    3. Priority for scheduling
    4. Constraints for safety
    5. Context for understanding
    
    Example:
        request = TaskRequest(
            task_id="task_123",
            tool_id="web_scraper",
            parameters={"url": "https://example.com", "selectors": ["h1", "p"]},
            priority="normal"
        )
    """
    model_config = ConfigDict(
        json_schema_extra={
            "example": {
                "task_id": "task_abc123",
                "tool_id": "web_scraper",
                "parameters": {"url": "https://example.com"},
                "priority": "normal",
                "constraints": {"max_execution_time_seconds": 30}
            }
        }
    )
    
    # Task identification
    task_id: str = Field(..., description="Unique task identifier (UUID)")
    plan_id: Optional[str] = Field(None, description="Parent plan ID if part of a plan")
    
    # What to execute
    tool_id: str = Field(..., description="Which tool/tentacle to use")
    parameters: Dict[str, Any] = Field(..., description="Tool-specific parameters")
    
    # Priority & scheduling
    priority: TaskPriority = Field(default=TaskPriority.NORMAL)
    
    # Constraints & guardrails
    constraints: TaskConstraints = Field(default_factory=TaskConstraints)
    
    # Context
    context: TaskContext = Field(default_factory=TaskContext)
    
    # Retry configuration
    max_retries: int = Field(default=3, ge=0, le=10)
    retry_delay_seconds: float = Field(default=1.0, ge=0)
    
    # Metadata
    created_at: datetime = Field(default_factory=datetime.now)
    created_by: str = Field(default="brain", description="Who created this task")
    description: Optional[str] = Field(None, description="Human-readable description")
    tags: List[str] = Field(default_factory=list)


# ============================================================
# PLAN MODELS (Brain's Output)
